pi = 3.14
def area(radius):
    return pi * (radius**2)
def circumference(radius):
    return 2 * pi * radius
def SphereSurface(radius):
    return 4 * area(radius)
def SphereVol(radius):
    return (4.0/3.0) * pi * (radius**3)
def cubeIt(x):
    return x**3
